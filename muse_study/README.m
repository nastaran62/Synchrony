1- Run the Controller_muse_timer.py script to start the Muse data collection with timer control.
-pipenv run python muse_study/Controller_muse_timer.py TM T p3
-pipenv run python muse_study/Controller_muse_timer.py FM F p3
2- Run muse sdk app in an android device
3- Add pc ip address and muse port (default 5700) in the muse sdk app
4- make sure both are at the same network
5- Press refresh button to see the muse devices in the list
6- chooose the muse device and press connect
7- After connection is established and you could see the muse address and first record, continue using the senario windows